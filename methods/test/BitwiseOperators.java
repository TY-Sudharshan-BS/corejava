package com.tyss.basic.test;

public class BitwiseOperators {
public static void main(String[] args) {
	//and operator
	System.out.println(4&5);
	
	//or operator
	System.out.println(4|5);
	
	//x-or operator
	System.out.println(4^5);
	
	//negation
//	System.out.println(~true);
	
	//negation of integers
	System.out.println(~4); //-5
}
}
