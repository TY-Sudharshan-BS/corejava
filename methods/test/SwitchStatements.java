package com.tyss.basic.test;

public class SwitchStatements {
public static void main(String[] args) {
	int num = 2;
	switch(num) {
	case 1: System.out.println("10");
	break;
	case 2: System.out.println("20");
	break;
	case 3: System.out.println("30");
	break;
	default: System.out.println("it is a different number");
} //end of switch
}// end of main method
 //end of class
}
