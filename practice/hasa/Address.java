package com.tyss.practice.hasa;

public class Address {
	int door_no;
	String street_name;
	String area;
	String city;
	int pincode;
	public Address(int door_no, String street_name, String area, String city, int pincode) {
		this.door_no = door_no;
		this.street_name = street_name;
		this.area = area;
		this.city = city;
		this.pincode = pincode;
	}
}
